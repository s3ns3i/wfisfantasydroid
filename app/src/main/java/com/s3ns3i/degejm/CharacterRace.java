package com.s3ns3i.degejm;



public class CharacterRace {


    private String raceName;
    private Integer raceID;
    private Integer baseHP;
    private Integer baseMana;
    private Integer STR;
    private Integer DEX;
    private Integer AGI;

    public String getRaceName_() {
        return raceName;
    }

    public void setRaceName_(String raceName) {
        this.raceName = raceName;
    }

    public Integer getRaceID() {
        return raceID;
    }

    public void setRaceID(Integer raceID) {
        this.raceID = raceID;
    }

    public Integer getBaseHP() {
        return baseHP;
    }

    public void setBaseHP(Integer baseHP) {
        this.baseHP = baseHP;
    }

    public Integer getBaseMana() {
        return baseMana;
    }

    public void setBaseMana(Integer baseMana) {
        this.baseMana = baseMana;
    }

    public Integer getSTR() {
        return STR;
    }

    public void setSTR(Integer STR) {
        this.STR = STR;
    }

    public Integer getDEX() {
        return DEX;
    }

    public void setDEX(Integer DEX) {
        this.DEX = DEX;
    }


    public Integer getAGI() {
        return AGI;
    }

    public void setAGI(Integer AGI) {
        this.AGI = AGI;
    }



    public CharacterRace() {

    }
}
